#!/usr/bin/env python3
# Test cases for floor division and modulo operators

# Test case 1: -7 // 2
result1 = -7 // 2
print(result1)  # Expected: -4

# Test case 2: -7 % 2
result2 = -7 % 2
print(result2)  # Expected: 1

# Test case 3: -5 // 3
result3 = -5 // 3
print(result3)  # Expected: -2

# Test case 4: -5 % 3
result4 = -5 % 3
print(result4)  # Expected: 1

# Test case 5: 7 // -2
result5 = 7 // -2
print(result5)  # Expected: -4

# Test case 6: 7 % -2
result6 = 7 % -2
print(result6)  # Expected: -1
